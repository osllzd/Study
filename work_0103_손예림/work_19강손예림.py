# 중첩 루프 사용하여 사각형으로 별 모양 출력하기
for i in range(5): # 5번 반복 바깥쪽 루프는 세로 방향
    for j in range(5): # 5번 반복 안쪽 루프는 가로 방향
        print('*',end='') # 가로 방향으로 별 다 그린 뒤 다음 줄로 넘어감
    print()

# 계단식으로 별 출력하기
for i in range(5): # 0부터 4까지 5번 반복 세로방향
    for j in range(5): # 0부터 4까지 5번 반복 가로방향
        if j <=i: # 세로 방향인 변수 i만큼
            print('*',end='')
    print()

# 대각선으로 별 출력하기
for i in range(5):
    for j in range(5):
        if j == i:
            print('*',end='')
        else:
            print(' ',end='')
    print()

