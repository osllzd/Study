# 함수 기능: 팩토리얼을 계산 후 계산 결과를 반환해주는 기능
#           3! = 3 * 2*  1
# 함수 이름: factorial
# 매개 변수: x
# 반 환 값: 계산 str
#-------------------------------------------------------------
def fact(x):
    if x == 0:
        return 1 # x=0 일때 1이 반환됨
    while x:
        print(f'{x}! = {x}*{x-1}')
        return x*fact(x-1)
print(fact(3))

