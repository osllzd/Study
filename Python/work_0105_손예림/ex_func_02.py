# 함수 기능: 팩토리얼을 계산 후 계산 결과를 반환해주는 기능
#           3! = 3 * 2*  1
# 함수 이름: factorial
# 매개 변수: x
# 반 환 값: 팩토리얼
#-------------------------------------------------------------


# for 문
def fact(x):
    ret = 1
    if x:
        for n in range(x,0,-1): ret *= n
        return ret
print(fact(4))

