# 시키고 싶은 일: 숫자, 글자 데이터를 화면에 출력하기
# 출력하기 => print(데이터)
# 숫자 데이터 출력 => 정수 int
print(2024)
print(1227)
print(33)

# 숫자 데이터 출력 => 실수 float
print(2.9)
print(5.123)
print(1.) #숫자 뒤에 .이 있을 시 실수

#문자/글자/문자열 데이터 출력 => 글자 str
# '데이터', "데이터"
print("홍길동")
print('지금은 겨울')
print("Happy new year")

# 여러줄 문자열 표시 ''' or """ 로 시작함
print('''good
luck
happy''')
